import type { IModifier, ModifierType } from "module/modifiers";
import type { ICostModifier } from "module/util/costs/spellCostManagement";
import type SplittermondItem from "module/item/item";
import type { AddModifierResult } from "module/modifiers/modifierAddition";
import { ModifierDataModel } from "./dataModel/ModifierDataModel";
import { CostModifierDataModel } from "./dataModel/CostModifierDataModel";
import type { EffectType } from "./dataModel/effectTypes";

type AddModifierFn = (
    item: SplittermondItem,
    str: string,
    type: ModifierType,
    multiplier: number
) => AddModifierResult;

interface EffectCreationData {
    name: string;
    origin: string;
    type: EffectType;
    transfer: boolean;
    disabled: boolean;
    system: object;
    flags: Record<string, unknown>;
}

function buildScalarEffectData(
    modifier: IModifier,
    rawFragment: string,
    origin: string
): EffectCreationData {
    const system = ModifierDataModel.init(
        modifier.groupId,
        modifier.value,
        modifier.attributes,
        modifier.selectable
    );
    return {
        name: rawFragment,
        origin,
        type: modifier.effectType,
        transfer: true,
        disabled: false,
        system,
        flags: {
            splittermond: {
                autoGenerated: true,
                rawInput: rawFragment,
            },
        },
    };
}

function buildCostEffectData(
    modifier: ICostModifier,
    rawFragment: string,
    origin: string
): EffectCreationData {
    const system = CostModifierDataModel.init(
        modifier.label,
        modifier.value,
        modifier.skill,
        modifier.attributes
    );
    return {
        name: rawFragment,
        origin,
        type: modifier.effectType,
        transfer: true,
        disabled: false,
        system,
        flags: {
            splittermond: {
                autoGenerated: true,
                rawInput: rawFragment,
            },
        },
    };
}

/**
 * Add ActiveEffects from a modifier string to an item, without deleting existing effects.
 */
export async function addModifierEffects(
    addModifier: AddModifierFn,
    item: SplittermondItem,
    modifierString: string,
    modifierType: ModifierType = "magic" as ModifierType,
    multiplier: number = 1
): Promise<void> {
    if (!modifierString.trim()) return;

    const { modifiers, costModifiers } = addModifier(item, modifierString, modifierType, multiplier);

    const effectDataArray: EffectCreationData[] = [
        ...modifiers.map(({ modifier, rawFragment }) =>
            buildScalarEffectData(modifier, rawFragment, item.uuid)
        ),
        ...costModifiers.map(({ modifier, rawFragment }) =>
            buildCostEffectData(modifier, rawFragment, item.uuid)
        ),
    ];

    if (effectDataArray.length > 0) {
        await (item as any).createEmbeddedDocuments("ActiveEffect", effectDataArray);
    }
}

/**
 * Rebuild the auto-generated ActiveEffects on an item from its modifier string.
 * Deletes existing auto-generated effects, then creates new ones from the current string.
 */
export async function rebuildModifierEffects(
    addModifier: AddModifierFn,
    item: SplittermondItem,
    modifierType: ModifierType,
    multiplier: number = 1
): Promise<void> {
    const modifierString: string = (item.system as any).modifier ?? "";

    const existingAutoEffects = (item as any).effects.filter(
        (e: any) => e.getFlag("splittermond", "autoGenerated") === true
    );
    const idsToDelete: string[] = existingAutoEffects.map((e: any) => e.id);
    if (idsToDelete.length > 0) {
        await (item as any).deleteEmbeddedDocuments("ActiveEffect", idsToDelete);
    }

    if (!modifierString.trim()) return;

    const { modifiers, costModifiers } = addModifier(item, modifierString, modifierType, multiplier);

    const effectDataArray: EffectCreationData[] = [
        ...modifiers.map(({ modifier, rawFragment }) =>
            buildScalarEffectData(modifier, rawFragment, item.uuid)
        ),
        ...costModifiers.map(({ modifier, rawFragment }) =>
            buildCostEffectData(modifier, rawFragment, item.uuid)
        ),
    ];

    if (effectDataArray.length > 0) {
        await (item as any).createEmbeddedDocuments("ActiveEffect", effectDataArray);
    }
}
